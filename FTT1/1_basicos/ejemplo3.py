# una variables puede almacenar cualquier tipo de dato.

#texto:
variable = "casa"


#numerico entero:
variable = 5



#numerico decimal:
variable = 5.5


# normalmente python se encarga de darse cuenta el tipo del dato
# pero aveces necesitamos espesificarle cual tipo es

#texto:
variable = str("casa")
print("Me voy a mi" + variable)

#numerico entero:
variable = int(5)
print(10 + variable)


#numerico decimal:
variable = float(5.5)
print(10 + variable)

# en estas lineas hemos usado las funciones de python "str", "int" y "float"
# para espesificar el tipo de dato antes de guardarlo en la variable.

