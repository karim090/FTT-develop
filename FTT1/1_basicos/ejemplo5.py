# tipos de datos booleanos y condiciones
# los tipos de datos booleanos (True, False) se usan para tomar desiciones
# una variable puede ser tambien un dato booleano

variable = True

# para crear una condicion usamos el bloques "if" 
# "else" y "elif" son opcionales

if variable:
    print("la variable de arriba es True")
else:
    print("la variable de arriba es False")

numero_ingresado = int(input("Ingresar un numero menor a 500:"))

if numero_ingresado < 500:
    print("Ingresate un numero menor a 500")
else:
    print("Ingresate un numero mayor a 500")

